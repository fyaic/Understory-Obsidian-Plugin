function getLanguage(pluginOrSettings) {
    const settings = pluginOrSettings && pluginOrSettings.settings ? pluginOrSettings.settings : pluginOrSettings;
    if (settings && settings.uiLanguage === 'zh') return 'zh';
    if (settings && settings.uiLanguage === 'en') return 'en';
    return DEFAULT_LANGUAGE;
}

function interpolate(template, values) {
    if (!values) return template;
    return String(template).replace(/\{(\w+)\}/g, (match, key) => (
        Object.prototype.hasOwnProperty.call(values, key) ? String(values[key]) : match
    ));
}

function t(pluginOrSettings, key, values) {
    const lang = getLanguage(pluginOrSettings);
    const template = (STRINGS[lang] && STRINGS[lang][key]) || STRINGS[DEFAULT_LANGUAGE][key] || key;
    return interpolate(template, values);
}

module.exports = { DEFAULT_LANGUAGE, STRINGS, getLanguage, t };
